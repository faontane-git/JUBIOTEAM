    public void createScene() {
        Platform.runLater(new Runnable() {
            @Override
            public void run() {
                File file = new File("src/Fotos/videoHormigas.mp4");
                createVideo = new MediaPlayer(
                        new Media(file.toURI().toString())
                );
                jfxPanel.setScene(new Scene(new Group(new MediaView(createVideo))));
                createVideo.setVolume(1);
                createVideo.play();
            }
        });
    }

